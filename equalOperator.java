public class equalOperator {
    
    public static void main(String[] args) {
        
        boolean result= false;
        if(result==false) {
            System.out.println("This is not a valid");
        }

        //Ternary Operator
        result=true;
        boolean isresult = result? true : false;
        if(isresult) {
            System.out.println("isresult is true");
        }
    }
}
