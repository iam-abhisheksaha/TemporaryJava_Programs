public class peratorsAndOperand2 {
    
    public static void main(String[] args){

        int result=5;
        result++; //5+1=6
        System.out.println("5+1= "+ result);

        result--; //6-1=5
        System.out.println("6-1= "+ result);

        //result=result+2
        result+=2; // result=result+2
        System.out.println("5+2 = "+ result);

        //result=result-2
        result-=2; // result=result-2
        System.out.println("7-2 = "+ result);

        //result=result*10
        result*=10;
        System.out.println("5*10 = "+ result);

        //result=result/10
        result/=5;
        System.out.println("50/5= "+ result);
    }
}
